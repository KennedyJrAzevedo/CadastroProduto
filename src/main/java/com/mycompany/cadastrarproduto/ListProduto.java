    private void jButtonExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonExcluirActionPerformed
        String[] options = new String[]{"Sim", "Cancelar"};
        int option = JOptionPane.showOptionDialog(null, "Deseja excluir o registro?", "Confirmar Exclus√£o",
                JOptionPane.WARNING_MESSAGE, JOptionPane.NO_OPTION,
                null, options, options[1]);
        if (option == 0) {
            int row = jTable1.getSelectedRow();
         
            produto.setId_produto(Integer.parseInt(jTable1.getValueAt(row,0).toString()));
            produto.excluirProduto();  
        }
    }//GEN-LAST:event_jButtonExcluirActionPerformed

    private void jButtonEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEditarActionPerformed
        int row = jTable1.getSelectedRow();
        jTextFieldNome.setText(jTable1.getValueAt(row, 0).toString());
        jComboBoxCategoria.setSelectedItem(jTable1.getValueAt(row, 1));
        jTextAreaDescricao.setText(jTable1.getValueAt(row, 2).toString());
        jTextFieldCodBarra.setText(jTable1.getValueAt(row, 3).toString());
        jComboBoxMarca.setSelectedItem(jTable1.getValueAt(row, 4));
        jTextFieldPeso.setText(jTable1.getValueAt(row, 5).toString());
        jTextFieldPreco.setText(jTable1.getValueAt(row, 6).toString());
        jButtonCadastrar.setText("Alterar");
        
    }//GEN-LAST:event_jButtonEditarActionPerformed

